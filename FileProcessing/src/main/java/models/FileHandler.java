package models;

import java.io.BufferedReader;
import java.io.BufferedWriter;
import java.io.FileReader;
import java.io.FileWriter;
import java.io.IOException;
import java.util.ArrayList;

/**
 *
 * @author quoch
 */
public class FileHandler {
    private String path;

    public FileHandler(String path) {
        this.path = path;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
    
    public ArrayList<String> readFile() throws IOException, Exception {
        ArrayList<String> lines = new ArrayList<>();
        if(path == null) throw new Exception("path is null");
        try (BufferedReader reader = new BufferedReader(new FileReader(path))) {
            String line;
            while ((line = reader.readLine()) != null) {
                lines.add(line);
            }
        }

        return lines;
    }
    
    public void writeFile(String filePath, String[] lines) throws IOException {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(filePath))) {
            for (String line : lines) {
                writer.write(line);
                writer.newLine(); // Thêm dòng mới sau mỗi dòng
            }
        }
    }
    
}
